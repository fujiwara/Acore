package Acore::MIME::Types;

use strict;
use warnings;

our $MIME_TYPES;

*mimeTypeOf = \&mime_type;

sub mime_type {
    my ($class, $file) = @_;
    $file =~ /\.?([a-z]+)$/i or return;
    $MIME_TYPES->{lc $1};
}

sub add_type {
    my $class = shift;
    while (my($ext, $type) = splice @_, 0, 2) {
        $MIME_TYPES->{lc $ext} = $type;
    }
}

